package introduction;

public class NumericalType {

	public static void main(String[] args) {
		
		/*
		 * General Syntax:		dataType variableName = value;
		 */
		int age = 35;
		
		System.out.println(age);
		
		/*
		 * reassignment int variable: 	variableName = newValue;
		 */
		age = 12;
		
		System.out.println(age);

	}

}
